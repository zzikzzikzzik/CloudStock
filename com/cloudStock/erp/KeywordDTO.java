package com.cloudStock.erp;

public class KeywordDTO {
	private String key_no;
	private String key_name;
	private String key_content;
	
	
	
	public String getKey_no() {
		return key_no;
	}
	public void setKey_no(String key_no) {
		this.key_no = key_no;
	}
	public String getKey_name() {
		return key_name;
	}
	public void setKey_name(String key_name) {
		this.key_name = key_name;
	}
	public String getKey_content() {
		return key_content;
	}
	public void setKey_content(String key_content) {
		this.key_content = key_content;
	}
	
	
}
